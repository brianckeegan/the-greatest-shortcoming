#!/usr/bin/env python3
"""
apply-rename.py — Stage 4 of the draft-reconciliation harness.

Execute metadata/rename-plan.json (produced by Stage 3) against the working tree.

    python3 bin/apply-rename.py             # DRY RUN — prints planned ops, writes nothing
    python3 bin/apply-rename.py --apply     # actually edits the tree (on your branch)

Every operation is derived from the plan + metadata; the script contains no
hard-coded book content. It is idempotent: a second --apply is a no-op.

Pipeline (in order):
  1. add jekyll-redirect-from (Gemfile + _config plugins) if any move needs a redirect
  2. file moves            git mv chapters/landing files
  3. text replacement      token (word-boundary) + phrase (display) maps, single-pass
  4. regenerate data       only files listed in plan.regen (e.g. cards.yml) from metadata
  5. new chapters          create stubs for chapters with status "new"
  6. chapter front matter   set num / redirect_from / source_note from metadata

Run Stage 3 (audit-site.py) FIRST so the plan reflects the current tree.
"""
from __future__ import annotations

import argparse
import json
import re
import subprocess
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
PLAN = REPO / "metadata" / "rename-plan.json"
META = REPO / "metadata" / "draft-metadata.json"


def make_re(mapping: dict, word_boundary: bool):
    if not mapping:
        return None
    body = "|".join(re.escape(k) for k in sorted(mapping, key=len, reverse=True))
    pat = rf"(?<![\w-])(?:{body})(?![\w-])" if word_boundary else f"(?:{body})"
    return re.compile(pat)


def sub(text: str, mapping: dict, rx) -> tuple[str, int]:
    if not rx:
        return text, 0
    count = 0

    def repl(m):
        nonlocal count
        count += 1
        return mapping[m.group(0)]

    return rx.sub(repl, text), count


def yaml_scalar(v) -> str:
    """JSON is a YAML 1.2 subset for flow scalars, so json.dumps is safe quoting."""
    return json.dumps(v, ensure_ascii=False)


def git_mv(src: str, dst: str, apply: bool) -> None:
    if not apply:
        return
    if not (REPO / src).exists() and (REPO / dst).exists():
        return  # already moved — keep --apply idempotent
    subprocess.run(["git", "-C", str(REPO), "mv", src, dst], check=True)


def patch_frontmatter(path: Path, sets: dict, list_add: dict, apply: bool) -> bool:
    """Set scalar front-matter keys and append values to list keys. Returns True
    if anything would change."""
    text = path.read_text(encoding="utf-8")
    m = re.match(r"^---\n(.*?)\n---\n?(.*)$", text, re.S)
    if not m:
        return False
    fm, body = m.group(1), m.group(2)
    lines = fm.split("\n")
    changed = False

    for key, val in sets.items():
        line = f"{key}: {yaml_scalar(val)}"
        for i, ln in enumerate(lines):
            if re.match(rf"^{re.escape(key)}\s*:", ln):
                if ln != line:
                    lines[i] = line
                    changed = True
                break
        else:
            lines.append(line)
            changed = True

    for key, vals in list_add.items():
        present = any(re.match(rf"^{re.escape(key)}\s*:", ln) for ln in lines)
        if not present:
            lines.append(f"{key}: [{', '.join(yaml_scalar(v) for v in vals)}]")
            changed = True

    if changed and apply:
        path.write_text("---\n" + "\n".join(lines) + "\n---\n" + body, encoding="utf-8")
    return changed


def regen_file(rel: str, meta: dict) -> str:
    chapters = meta["chapters"]
    if rel.endswith("chapters.yml"):
        out = ["# Generated by bin/apply-rename.py from metadata/draft-metadata.json.",
               "# Drives the table of contents, chapter cards, and prev/next nav.", ""]
        for ch in chapters:
            out += [f"- num: {yaml_scalar(ch['num'])}",
                    f"  slug: {ch['slug']}",
                    f"  title: {yaml_scalar(ch['title'])}",
                    f"  status: drafted",
                    f"  pitch: {yaml_scalar(ch.get('pitch') or ch['summary'][:160])}"]
        return "\n".join(out) + "\n"
    if rel.endswith("cards.yml"):
        out = ["# Generated by bin/apply-rename.py from metadata/draft-metadata.json.",
               "# Homepage chapter-card metadata, keyed by chapter slug.", ""]
        for ch in chapters:
            out += [f"{ch['slug']}:",
                    f"  era: {yaml_scalar(ch.get('era_label') or '')}",
                    f"  concept: {yaml_scalar(ch.get('concept') or '')}",
                    f"  teaser: {yaml_scalar(ch.get('teaser') or '')}"]
        return "\n".join(out) + "\n"
    raise ValueError(f"don't know how to regenerate {rel}")


def finalize_metadata(meta: dict) -> tuple[dict, list[str]]:
    """Return (settled_meta, change_notes). After a rename has been applied to the
    site, the committed metadata should mirror the *current* site so the next
    draft's audit starts clean (#1). This settles prev_* == current and resets
    statuses to 'unchanged', while KEEPING aliases_deprecated/preserve_terms as
    regression guards (those still protect after the rename settles, see #2), and
    clears the one-shot extra_renames escape hatch. Pure transform; caller writes."""
    import copy
    m = copy.deepcopy(meta)
    notes: list[str] = []

    for c in m.get("constructs", []):
        for prev, cur in (("prev_id", "id"), ("prev_name", "canonical_name"),
                          ("prev_slug", "slug"), ("prev_short_label", "short_label")):
            if prev in c and c.get(prev) not in (None, c.get(cur)):
                c[prev] = c.get(cur)
                notes.append(f"construct {c.get('id')}: {prev} → {c.get(cur)!r}")
        if c.get("status") == "renamed":
            c["status"] = "unchanged"
            notes.append(f"construct {c.get('id')}: status renamed → unchanged "
                         f"(aliases_deprecated kept as guard)")

    for ch in m.get("chapters", []):
        for prev, cur in (("prev_slug", "slug"), ("prev_title", "title"),
                          ("prev_era_id", "era_id")):
            if prev in ch and ch.get(prev) != ch.get(cur):
                ch[prev] = ch.get(cur)
                notes.append(f"chapter {ch.get('slug')}: {prev} → {ch.get(cur)!r}")
        if ch.get("status") != "unchanged":
            notes.append(f"chapter {ch.get('slug')}: status {ch.get('status')} → unchanged")
            ch["status"] = "unchanged"

    extra = m.get("extra_renames")
    if extra and (extra.get("tokens") or extra.get("phrases")):
        notes.append("extra_renames cleared (one-shot renames already applied)")
        m["extra_renames"] = {"tokens": {}, "phrases": {}}

    return m, notes


def new_chapter_text(ch: dict, meta: dict) -> str:
    src = meta["draft"]["source_pdf"]
    date = meta["draft"]["ingested"]
    fm = ["---", f"slug: {ch['slug']}", f"num: {yaml_scalar(ch['num'])}",
          f"title: {yaml_scalar(ch['title'])}",
          f"source_note: {yaml_scalar(f'Stub generated from {src} ({date}); body pending.')}",
          "---", "", f"<p>{ch['summary']}</p>", ""]
    return "\n".join(fm)


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--apply", action="store_true", help="write changes (default: dry run)")
    ap.add_argument("--finalize", action="store_true",
                    help="after a successful apply, rewrite metadata so prev_*==current "
                         "and settle statuses, making the committed metadata an honest "
                         "mirror of the site for the next draft (only writes with --apply)")
    args = ap.parse_args()
    apply = args.apply
    mode = "APPLY" if apply else "DRY RUN"

    if not PLAN.exists():
        sys.exit("no rename-plan.json — run bin/audit-site.py first")
    plan = json.loads(PLAN.read_text())
    meta = json.loads(META.read_text())

    token_map, phrase_map = plan["token_map"], plan["phrase_map"]
    tok_rx, phr_rx = make_re(token_map, True), make_re(phrase_map, False)
    moves = plan["file_moves"]
    move_from = {m["from"]: m for m in moves}

    print(f"=== {mode} — draft v{plan['draft_version']} ===\n")

    # 1. redirect plugin
    needs_redirect = any("redirect_from" in m for m in moves)
    if needs_redirect:
        gem = (REPO / "Gemfile").read_text() if (REPO / "Gemfile").exists() else ""
        have = "jekyll-redirect-from" in gem
        print(f"[plugin] jekyll-redirect-from: {'already present' if have else 'WOULD ADD to Gemfile + _config plugins'}")
        if apply and not have:
            _add_redirect_plugin()

    # 2. moves
    print(f"\n[moves] {len(moves)}")
    for m in moves:
        print(f"  {m['from']}  →  {m['to']}"
              + (f"   (redirect {m['redirect_from']})" if "redirect_from" in m else ""))
        if apply:
            git_mv(m["from"], m["to"], apply)

    # 3. text replacement over effective paths
    effective = []
    for f in plan["apply_files"]:
        effective.append(move_from[f]["to"] if f in move_from else f)
    effective += [m["to"] for m in moves if m["to"] not in effective]

    print(f"\n[text] token+phrase replacement over {len(effective)} files")
    # redirect_from intentionally holds OLD slugs — never rename it (keeps --apply idempotent)
    skip = re.compile(r"^\s*redirect_from\s*:")
    total, touched = 0, 0
    for f in effective:
        p = REPO / f
        if not p.exists():
            continue
        out, n = [], 0
        for line in p.read_text(encoding="utf-8").splitlines(keepends=True):
            if skip.match(line):
                out.append(line)
                continue
            line, a = sub(line, phrase_map, phr_rx)
            line, b = sub(line, token_map, tok_rx)
            out.append(line)
            n += a + b
        if n:
            touched += 1
            total += n
            print(f"  {f}: {n} change(s)")
            if apply:
                p.write_text("".join(out), encoding="utf-8")
    print(f"  → {total} replacements across {touched} files")

    # 4. regenerate data files
    print(f"\n[regen] {plan.get('regen', [])}")
    for rel in plan.get("regen", []):
        content = regen_file(rel, meta)
        print(f"  {rel}: regenerate ({len(content.splitlines())} lines)")
        if apply:
            (REPO / rel).write_text(content, encoding="utf-8")

    # 5. new chapters
    print(f"\n[new] {plan.get('new_chapters', [])}")
    for slug in plan.get("new_chapters", []):
        ch = next(c for c in meta["chapters"] if c["slug"] == slug)
        dest = REPO / "_chapters" / f"{slug}.md"
        print(f"  create _chapters/{slug}.md ({'exists!' if dest.exists() else 'new'})")
        if apply and not dest.exists():
            dest.write_text(new_chapter_text(ch, meta), encoding="utf-8")

    # 6. chapter front matter (num / redirect_from / source_note)
    print(f"\n[frontmatter] num + redirect + source_note")
    date = meta["draft"]["ingested"]
    for ch in meta["chapters"]:
        dest = REPO / "_chapters" / f"{ch['slug']}.md"
        if not dest.exists() and not apply:
            # after a dry-run the move hasn't happened; resolve via the plan
            mv = next((m for m in moves if m["to"] == f"_chapters/{ch['slug']}.md"), None)
            dest = REPO / (mv["from"] if mv else f"_chapters/{ch['slug']}.md")
        if not dest.exists():
            continue
        sets = {"num": ch["num"],
                "source_note": f"Reconciled from {meta['draft']['source_pdf']} ({date})."}
        mv = next((m for m in moves if m["to"] == f"_chapters/{ch['slug']}.md"), None)
        if mv and "redirect_from" in mv:
            sets["redirect_from"] = mv["redirect_from"]  # scalar, overwritten → self-heals
        if patch_frontmatter(dest, sets, {}, apply):
            print(f"  {ch['slug']}: num={ch['num']}"
                  + (f" +redirect {mv['redirect_from']}" if mv and 'redirect_from' in mv else ""))

    # 7. finalize (opt-in): settle the committed metadata to mirror the site
    if args.finalize:
        settled, fnotes = finalize_metadata(meta)
        print(f"\n[finalize] {len(fnotes)} metadata setting(s) to settle prev_* → current")
        for n in fnotes:
            print(f"  {n}")
        if not fnotes:
            print("  metadata already settled (nothing to do)")
        if apply and fnotes:
            META.write_text(json.dumps(settled, indent=2, ensure_ascii=False) + "\n",
                            encoding="utf-8")
            print(f"  → wrote {META.relative_to(REPO)}")
        elif fnotes:
            print("  (dry run — pass --apply to write settled metadata)")

    print(f"\n=== {mode} complete ===")
    if not apply:
        print("nothing was written. Re-run with --apply to execute.")


def _add_redirect_plugin() -> None:
    gemf = REPO / "Gemfile"
    g = gemf.read_text()
    if "jekyll-redirect-from" not in g:
        g = g.rstrip() + '\ngem "jekyll-redirect-from", "~> 0.16"\n'
        gemf.write_text(g)
    cfg = REPO / "_config.yml"
    c = cfg.read_text()
    if "jekyll-redirect-from" not in c:
        c = re.sub(r"(plugins:\n)", r"\1  - jekyll-redirect-from\n", c, count=1)
        cfg.write_text(c)


if __name__ == "__main__":
    main()
